package turotial.models;

public class StudentModel {

    private String name;
    private int score;

    public StudentModel() {
    }

    public StudentModel(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
